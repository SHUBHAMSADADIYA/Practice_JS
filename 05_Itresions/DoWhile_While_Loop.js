
// While Loop
i = 0;
while (i <= 10) {
    console.log(`Value of i is ${i}`);
    i = i + 2
}

// While loop from Array
let myArray = ["Shubham", "Bhargav", "Prince", "Rohit", "Mihir"]
let arr = 0;

while (arr < myArray.length) {
    console.log(`Name is ${myArray[arr]}`);
    arr++; 
}

// Do While Loop
score = 0
do {
    console.log(`Score is ${score}`);
    score = score + 5
} while (score <= 25);